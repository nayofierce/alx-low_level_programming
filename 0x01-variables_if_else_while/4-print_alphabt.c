#include <stdio.h>
/**
 *main - Entry point
 *
 * Return: always 0 (Success)
 */
int main(void)
{
char letter;
for ((letter = 'a'); (letter <= 'z'); letter++)
while (letter == 'q') /*continue if is q*/
{
continue;
}
while (letter == 'e') /*continue if is q*/
{
continue;
}
{
putchar(letter);
putchar('\n');
}
return (0);
}
